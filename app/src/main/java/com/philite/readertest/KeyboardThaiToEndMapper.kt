            'เ' -> 'g'
            '้' -> 'h'
            '่' -> 'j'
            'า' -> 'k'
            'ส' -> 'l'
            'ว' -> ';'
            'ง' -> '\''
            'ๆ' -> 'q'
            'ไ' -> 'w'
            'ำ' -> 'e'
            'พ' -> 'r'
            'ะ' -> 't'
            'ั' -> 'y'
            'ี' -> 'u'
            'ร' -> 'i'
            'น' -> 'o'
            'ย' -> 'p'
            'บ' -> '['
            'ล' -> ']'
            'ฃ' -> '\\'
            'ๅ' -> '1'
            '/' -> '2'
            '-' -> '3'
            'ภ' -> '4'
            'ถ' -> '5'
            'ุ' -> '6'
            'ึ' -> '7'
            'ค' -> '8'
            'ต' -> '9'
            'จ' -> '0'
            'ข' -> '-'
